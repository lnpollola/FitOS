export function getSportDisplayName(type) {
  return strings.activity.sportNames[type] || type || 'Otro';
}

export function getMeasurementLabel(metric) {
  const key = MEASUREMENT_MAP[metric];
  if (key && strings.measurements[key]) return strings.measurements[key];
  return metric.replace(/_/g, ' ');
}
