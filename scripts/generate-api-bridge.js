const fs = require('fs');
const path = require('path');
const { channels } = require('../src/shared/api-channels');

const MANUAL_START = '// __MANUAL_START__';
const MANUAL_END = '// __MANUAL_END__';

function readManualBlock(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    const startIdx = content.indexOf(MANUAL_START);
    const endIdx = content.indexOf(MANUAL_END);
    if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
      return content.substring(startIdx + MANUAL_START.length, endIdx);
    }
  } catch {}
  return null;
}

function trimManual(manual) {
  return manual.replace(/\s+$/, '');
}

function generatePreload(allChannels, existingContent) {
  const rawManual = readManualBlock(existingContent) || `
  navigate: (view) => ipcRenderer.send('navigate', view),
  onNavigate: (callback) => { ipcRenderer.removeAllListeners('navigate'); ipcRenderer.on('navigate', (_event, view) => callback(view)); },
  onDataChanged: (callback) => { ipcRenderer.removeAllListeners('data-changed'); ipcRenderer.on('data-changed', () => callback()); },
  onHealthImportProgress: (callback) => ipcRenderer.on('health-import-progress', (_event, msg) => callback(msg)),
  removeHealthImportProgressListener: () => ipcRenderer.removeAllListeners('health-import-progress'),
  exportData: () => ipcRenderer.invoke('export:data'),
  importData: () => ipcRenderer.invoke('import:data'),
`;
  const manual = trimManual(rawManual);

  const lines = [];
  for (const ch of allChannels) {
    const params = ch.args === 0 ? '()' : ch.args === 1 ? '(arg)' : '(...args)';
    const argsPass = ch.args === 0 ? '' : ch.args === 1 ? 'arg' : 'args';
    lines.push(`  ${ch.method}: ${params} => ipcRenderer.invoke('${ch.channel}'${ch.args > 0 ? ', ' + argsPass : ''}),`);
  }

  return `const { contextBridge, ipcRenderer } = require('electron');

// ═══════════════════════════════════════════════════════════════
// AUTO-GENERATED by scripts/generate-api-bridge.js
// Do not edit manually — edit src/shared/api-channels.js instead
// ═══════════════════════════════════════════════════════════════

contextBridge.exposeInMainWorld('electronAPI', {
  // ── DOMAIN EVENTS (manual; preserved across regeneration) ──
  ${MANUAL_START}${manual}
  ${MANUAL_END}

  // ── GENERATED CRUD CHANNELS (${allChannels.length} methods) ──
${lines.join('\n')}
});
`;
}

function generateWebApi(allChannels, existingContent) {
  const rawManual = readManualBlock(existingContent) || `
  navigate: () => {},
  onNavigate: () => {},
  onDataChanged: () => {},
  onHealthImportProgress: () => {},
  removeHealthImportProgressListener: () => {},
  exportData: async () => {
    const res = await fetch('/api/export');
    if (!res.ok) throw new Error('Export failed: ' + res.status);
    return res.json();
  },
  importData: async () => {
    const input = document.createElement('input');
    input.type = 'file';
    input.accept = '.json';
    return new Promise((resolve, reject) => {
      input.onchange = async (e) => {
        const file = e.target.files[0];
        if (!file) return resolve(null);
        const text = await file.text();
        const data = JSON.parse(text);
        const res = await fetch('/api/import', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify(data),
        });
        if (!res.ok) { reject(new Error('Import failed: ' + res.status)); return; }
        resolve(res.json());
      };
      input.click();
    });
  },
`;
  const manual = trimManual(rawManual);

  const lines = [];
  for (const ch of allChannels) {
    const params = ch.args === 0 ? '()' : ch.args === 1 ? '(arg)' : '(...args)';
    if (ch.args === 0) {
      lines.push(`  async ${ch.method}() {
    const res = await fetch('/api/${ch.channel}', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ args: [] }) });
    if (!res.ok) throw new Error('API error: ' + res.status);
    return res.json();
  },`);
    } else if (ch.args === 1) {
      lines.push(`  async ${ch.method}(arg) {
    const res = await fetch('/api/${ch.channel}', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ args: [arg] }) });
    if (!res.ok) throw new Error('API error: ' + res.status);
    return res.json();
  },`);
    } else {
      lines.push(`  async ${ch.method}(...args) {
    const res = await fetch('/api/${ch.channel}', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ args: args }) });
    if (!res.ok) throw new Error('API error: ' + res.status);
    return res.json();
  },`);
    }
  }

  const routes = [...new Set(allChannels.map(ch => `/api/${ch.channel}`))].sort();

  return `// ═══════════════════════════════════════════════════════════════
// AUTO-GENERATED by scripts/generate-api-bridge.js
// Do not edit manually — edit src/shared/api-channels.js instead
// ═══════════════════════════════════════════════════════════════

export function getAPI() {
  if (window.electronAPI) return window.electronAPI;
  return webAPI;
}

export const webAPI = {
  // ── DOMAIN EVENTS (manual; preserved across regeneration) ──
  ${MANUAL_START}${manual}
  ${MANUAL_END}

  // ── GENERATED CRUD CHANNELS (${lines.length} methods) ──
${lines.join('\n')}
};

// ── ROUTE REGISTRATION (validates API surface against server) ──
export const apiRoutes = [
${routes.map(r => `  '${r}',`).join('\n')}
];
`;
}

function generatePreloadFromManifest() {
  const preloadPath = path.join(__dirname, '..', 'src', 'preload', 'preload.js');
  const content = generatePreload(channels, preloadPath);
  fs.writeFileSync(preloadPath, content);
  const methodCount = (content.match(/=> ipcRenderer\.invoke/g) || []).length;
  console.log('Generated preload.js (' + methodCount + ' channels)');
}

function generateWebApiFromManifest() {
  const webApiPath = path.join(__dirname, '..', 'src', 'renderer', 'utils', 'web-api.js');
  const content = generateWebApi(channels, webApiPath);
  fs.writeFileSync(webApiPath, content);
  const methodCount = (content.match(/async \w+[ (]/g) || []).filter(m => !m.includes('getAPI')).length;
  console.log('Generated web-api.js (' + (content.match(/\/\/ ── GENERATED/g) ? linesCount(content) : '?') + ')');
}

function linesCount(content) {
  const match = content.match(/\/\/ ── GENERATED CRUD CHANNELS \((\d+) methods\)/);
  return match ? match[1] : '?';
}

function main() {
  generatePreloadFromManifest();
  generateWebApiFromManifest();
  const preloadPath = path.join(__dirname, '..', 'src', 'preload', 'preload.js');
  const webApiPath = path.join(__dirname, '..', 'src', 'renderer', 'utils', 'web-api.js');
  const pc = fs.readFileSync(preloadPath, 'utf-8');
  const wc = fs.readFileSync(webApiPath, 'utf-8');
  console.log('Generated preload.js (' + (pc.match(/ipcRenderer\.invoke/g) || []).length + ' channels)');
  console.log('Generated web-api.js (' + (wc.match(/async \w+[ (]/g) || []).filter(m => !m.includes('getAPI')).length + ' methods)');
}

if (require.main === module) main();
module.exports = { generatePreload, generateWebApi, readManualBlock };
